"""E3. Defina una función recursiva que reciba un nombre(str) en minúsculas y que lo devuelva con las vocales en mayúsculas"""

def min_a_may(s, k = ""):
  j = "aeiou"
  if s is not None:
    for m in s:
      if m in j:
        t = m.upper()
        k += t
        return min_a_may(s[1:], k)
      else:
        k += m
        return min_a_may(s[1:], k)
    return k

