"""E6. Escriba una función recursiva que encuentre el mínimo de una lista L dada"""

def min(l):
  if len(l) == 0:
    return 0
  elif len(l) == 1:
    return l[0]
  else:
    m = min(l[1:])
    if m < l[0]:
      return m
    else:
      return l[0]

min([6, 3, 8, 9, 1])

