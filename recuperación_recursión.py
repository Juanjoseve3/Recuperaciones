"""E4. Escriba una función recursiva que imprima los número desde un número n hasta otro número m descendentemente"""

def imprimir_descendente(n, m):
  l = []
  if n and m is not None:
    for i in range(n, m+1):
      l.append(i)
    return l[::-1]

imprimir_descendente(4, 8)

